# A module is a collection of functions, something like a namespace. Every Elixir function must be defined inside a module.
# Elixir itself comes with many modules such as IO.puts which prints output to terminal.
IO.puts("Module is interesting.") # Here we are calling the puts function which is inside the IO module.

# To call a function to a module this is the syntax:
# ModuleName.function_name(args)
